package basics;

public class DataTypes {
    public static void main(String[] args) {

       
        // Primitive Data Types in Java
        

        // 1. int → integer numbers  
        int myInt = 37;
        System.out.println("int: " + myInt);

        // 2. byte → very small integer numbers 
        byte myByte = 120; // range: -128 to 127
        System.out.println("byte: " + myByte);

        // 3. short → medium-size integer numbers  
        short myShort = 32000;
        System.out.println("short: " + myShort);

        // 4. long → large integer numbers  
        long myLong = 9000000000L; // IMPORTANT: ends with L
        System.out.println("long: " + myLong);

        // 5. float → decimal numbers  
        float myFloat = 3.14f; // IMPORTANT: ends with f
        System.out.println("float: " + myFloat);

        // 6. double → more precise decimal numbers  
        double myDouble = 3.14159265;
        System.out.println("double: " + myDouble);

        // 7. char → a single character  
        char myChar = 'A';
        System.out.println("char: " + myChar);

        // 8. boolean → true or false values
        boolean myBoolean = true;
        System.out.println("boolean: " + myBoolean);



    }
}
